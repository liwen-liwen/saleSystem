//添加树视图
void CSelectView::OnInitialUpdate()
{
	CTreeView::OnInitialUpdate();

	// TODO:  在此添加专用代码和/或调用基类

	//加载图标
	HICON hicon = AfxGetApp()->LoadIconW(IDI_ICON1);

	//创建图标列表
	m_imageList.Create(30, 30, ILC_COLOR32, 1, 1);

	//将图标加载到图标列表中
	m_imageList.Add(hicon);
	//获取树控件
	m_tree = &GetTreeCtrl();

	//将图标列表加载到树控件中
	m_tree->SetImageList(&m_imageList, TVSIL_NORMAL);

	//创建节点
	m_tree->InsertItem(TEXT("个人信息"), 0, 0, NULL);
	m_tree->InsertItem(TEXT("销售管理"), 0, 0, NULL);
	m_tree->InsertItem(TEXT("库存信息"), 0, 0, NULL);
	m_tree->InsertItem(TEXT("库存增加"), 0, 0, NULL);
	m_tree->InsertItem(TEXT("库存删除"), 0, 0, NULL);



}


//选择响应
void CSelectView::OnTvnSelchanged(NMHDR *pNMHDR, LRESULT *pResult)
{
	LPNMTREEVIEW pNMTreeView = reinterpret_cast<LPNMTREEVIEW>(pNMHDR);
	// TODO:  在此添加控件通知处理程序代码

	HTREEITEM item = m_tree->GetSelectedItem();

	//获取节点名
	CString str = m_tree->GetItemText(item);

	//发送消息
	if (str == TEXT("个人信息"))
	{
		::PostMessage(AfxGetMainWnd()->GetSafeHwnd(), NM_A, (WPARAM)NM_A, (LPARAM)0);
		return;
	}
	else if (str == TEXT("销售管理"))
	{
		::PostMessage(AfxGetMainWnd()->GetSafeHwnd(), NM_B, (WPARAM)NM_B, (LPARAM)0);
		return;
	}
	else if (str == TEXT("库存信息"))
	{
		::PostMessage(AfxGetMainWnd()->GetSafeHwnd(), NM_C, (WPARAM)NM_C, (LPARAM)0);
		return;
	}
	else if (str == TEXT("库存增加"))
	{
		::PostMessage(AfxGetMainWnd()->GetSafeHwnd(), NM_D, (WPARAM)NM_D, (LPARAM)0);
		return;
	}
	else if (str == TEXT("库存删除"))
	{
		::PostMessage(AfxGetMainWnd()->GetSafeHwnd(), NM_E, (WPARAM)NM_E, (LPARAM)0);
		return;
	}

	*pResult = 0;


}
